package myBatis;

public class Kepzes {
	private int kid;
	private String knev;
	private String oktato;
	private int idotartam;
	
	public int getKid() {
		return kid;
	}
	
	public void setKid(int kid) {
		this.kid = kid;
	}
	
	public String getKnev() {
		return knev;
	}
	
	public void setKnev(String knev) {
		this.knev = knev;
	}
	
	public String getOktato() {
		return oktato;
	}
	
	public void setOktato(String oktato) {
		this.oktato = oktato;
	}
	
	public int getIdotartam() {
		return idotartam;
	}
	
	public void setIdotartam(int idotartam) {
		this.idotartam = idotartam;
	}
}
