package myBatis;

public class Kozos {
	private int did;
	private int kid;
	private String dnev;
	private String beosztas;
	private int fizetes;
	private String knev;
	private String oktato;
	private int idotartam;
	
	public int getDid() {
		return did;
	}
	
	public void setDid(int did) {
		this.did = did;
	}
	
	public String getDnev() {
		return dnev;
	}
	
	public void setDnev(String dnev) {
		this.dnev = dnev;
	}
	
	public String getBeosztas() {
		return beosztas;
	}
	
	public void setBeosztas(String beosztas) {
		this.beosztas = beosztas;
	}
	
	public int getFizetes() {
		return fizetes;
	}
	
	public void setFizetes(int fizetes) {
		this.fizetes = fizetes;
	}
	
	public int getKid() {
		return kid;
	}
	
	public void setKid(int kid) {
		this.kid = kid;
	}
	
	public String getKnev() {
		return knev;
	}
	
	public void setKnev(String knev) {
		this.knev = knev;
	}
	
	public String getOktato() {
		return oktato;
	}
	
	public void setOktato(String oktato) {
		this.oktato = oktato;
	}
	
	public int getIdotartam() {
		return idotartam;
	}
	
	public void setIdotartam(int idotartam) {
		this.idotartam = idotartam;
	}	
}
